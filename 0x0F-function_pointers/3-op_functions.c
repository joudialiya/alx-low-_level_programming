#include "3-calc.h"
/**
 * op_add - perform an operation
 * @a: first operand
 * @b: second operand
 *
 * Return: the result of the op
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - perform an operation
 * @a: first operand
 * @b: second operand
 *
 * Return: the result of the op
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - perform an operation
 * @a: first operand
 * @b: second operand
 *
 * Return: the result of the op
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - perform an operation
 * @a: first operand
 * @b: second operand
 *
 * Return: the result of the op
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - perform an operation
 * @a: first operand
 * @b: second operand
 *
 * Return: the result of the op
 */
int op_mod(int a, int b)
{
	return (a % b);
}
